def addition_op(number_1, number_2):
    return number_1 + number_2


def subtraction_op(number_1, number_2):
    return number_1 - number_2


def composition_op(number_1, number_2):
    return number_1 * number_2


def division_op(number_1, number_2):
    return number_1 / number_2


def int_div_op(number_1, number_2):
    return number_1 // number_2


def rem_div_op(number_1, number_2):
    return number_1 % number_2